class Tamil_calendar
def self.days1 num
   if num == "Monday"
      return "Thingal-kizamai"
   end
   if num == "Tuesday"
      return "Sevvaai-kizamai"
   end
   if num == "Wednesday"
       return "Budhan-kizamai"
   end
   if num == "Thursday"
     return "Vyaazha-kizamai"
  end
   if num == "Friday"
      return "velli-kizamai"
  end
   if num == "Saturday"
      return "sani-kizamai"
  end
   if num == "Sunday"
     return "Nyaayitru_kizhamai"
end
end
end
     
