class Days
def self.convert(number)
num = number.to_s
   if num == "Monday"
      return "Thingal-kizamai"
  
   elsif num == "Tuesday"
      return "Sevvaai-kizamai"
   
   elsif num == "Wednesday"
       return "Budhan-kizamai"
   
   elsif num == "Thursday"
     return "Vyaazha-kizamai"
  
   elsif num == "Friday"
      return "velli-kizamai"
  
  elsif num == "Saturday"
      return "sani-kizamai"
  
   elsif num == "Sunday"
     return "Nyaayitru_kizhamai"
end
end
end
     
